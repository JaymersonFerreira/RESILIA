SELECT TBC.NOME, TBZ.NOME, TBZ.CATEGORIA FROM TB_CLIENTE TBC
INNER JOIN TB_PEDIDO TBP ON TBC.ID_CLIENTE = TBP.ID_CLIENTE
INNER JOIN TB_PEDIDO_PIZZA TBPP ON TBP.ID_PEDIDO = TBPP.ID_PEDIDO
INNER JOIN TB_PIZZA TBZ ON TBPP.ID_PIZZA = TBZ.ID_PIZZA
WHERE TBZ.CATEGORIA LIKE '%Zero Lactose%'


SELECT TBC.NOME, SUM(TBP.PRECO) FROM TB_CLIENTE TBC
INNER JOIN TB_PEDIDO TBP ON TBC.ID_CLIENTE = TBC.ID_CLIENTE
GROUP BY TBC.NOME
ORDER BY SUM(TBP.PRECO) DESC



