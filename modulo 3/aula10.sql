SELECT TBC.NOME, TBP.PRECO, TBP.TIPO_ENTREGA FROM TB_CLIENTE TBC
INNER JOIN TB_PEDIDO TBP
ON TBP.ID_CLIENTE = TBC.ID_CLIENTE
WHERE TBP.TIPO_ENTREGA = 'Delivery'

SELECT TBC.NOME, TBZ.NOME FROM TB_CLIENTE TBC
INNER JOIN TB_PEDIDO TBP ON TBC.ID_CLIENTE = TBP.ID_CLIENTE
INNER JOIN TB_PEDIDO_PIZZA TBPP ON TBP.ID_PEDIDO = TBPP.ID_PEDIDO
INNER JOIN TB_PIZZA TBZ ON TBPP.ID_PIZZA = TBZ.ID_PIZZA

